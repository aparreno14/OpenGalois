def eval_all(*, p: Fraction, q: Fraction, r: Fraction, s: Fraction,
             theta: Fraction, D: Fraction) -> dict[str, Fraction]:
    """Evaluate F, T1, T2, T3, and T4 while reusing the same power tables."""
    p_pows, q_pows, r_pows, s_pows = _prepare_power_tables(p=p, q=q, r=r, s=s)

    F_value = _eval_compiled_sparse_4var(
        F_COMPILED,
        p_pows=p_pows,
        q_pows=q_pows,
        r_pows=r_pows,
        s_pows=s_pows,
    )
    if F_value == 0:
        raise ZeroDivisionError('Dummit appendix denominator F vanished.')

    twice_F = 2 * F_value
    twice_DF = 2 * D * F_value
    if twice_DF == 0:
        raise ZeroDivisionError('Dummit appendix denominator 2*D*F vanished.')

    return {
        'F': F_value,
        'T1': -_eval_theta_linear_combination_compiled(
            B1_COMPILED,
            theta,
            p_pows=p_pows,
            q_pows=q_pows,
            r_pows=r_pows,
            s_pows=s_pows,
        ) / twice_F,
        'T2': _eval_theta_linear_combination_compiled(
            B2_COMPILED,
            theta,
            p_pows=p_pows,
            q_pows=q_pows,
            r_pows=r_pows,
            s_pows=s_pows,
        ) / twice_DF,
        'T3': _eval_theta_linear_combination_compiled(
            B3_COMPILED,
            theta,
            p_pows=p_pows,
            q_pows=q_pows,
            r_pows=r_pows,
            s_pows=s_pows,
        ) / twice_F,
        'T4': _eval_theta_linear_combination_compiled(
            B4_COMPILED,
            theta,
            p_pows=p_pows,
            q_pows=q_pows,
            r_pows=r_pows,
            s_pows=s_pows,
        ) / twice_DF,
    }
