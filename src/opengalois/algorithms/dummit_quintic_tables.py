from __future__ import annotations

import re
from fractions import Fraction
from typing import Final

"""Shared Dummit quintic appendix tables for OpenGalois."""



Sparse4VarPoly = dict[tuple[int, int, int, int], Fraction]
CompiledSparse4VarPoly = tuple[tuple[int, int, int, int, Fraction], ...]

_TERM_SPLIT_RE: Final[re.Pattern[str]] = re.compile(r'[+-][^+-]+')
_COEFF_RE: Final[re.Pattern[str]] = re.compile(r'(\d+)')
_VAR_EXP_RE: Final[re.Pattern[str]] = re.compile(r'([pqrs])(\d*)')


def _power_table(x: Fraction, max_exp: int) -> list[Fraction]:
    out = [Fraction(1, 1)] * (max_exp + 1)
    for i in range(1, max_exp + 1):
        out[i] = out[i - 1] * x
    return out


def _compile_sparse_4var(poly: Sparse4VarPoly) -> CompiledSparse4VarPoly:
    return tuple((ep, eq, er, es, coeff) for (ep, eq, er, es), coeff in poly.items())


def _eval_compiled_sparse_4var(
    poly: CompiledSparse4VarPoly,
    *,
    p_pows: list[Fraction],
    q_pows: list[Fraction],
    r_pows: list[Fraction],
    s_pows: list[Fraction],
) -> Fraction:
    total = Fraction(0, 1)
    for ep, eq, er, es, coeff in poly:
        total += coeff * p_pows[ep] * q_pows[eq] * r_pows[er] * s_pows[es]
    return total


def eval_sparse_4var(poly: Sparse4VarPoly, *, p: Fraction, q: Fraction, r: Fraction,
                     s: Fraction) -> Fraction:
    """Evaluate a sparse polynomial in QQ[p,q,r,s] exactly."""
    if not poly:
        return Fraction(0, 1)

    max_p = max(ep for ep, _, _, _ in poly)
    max_q = max(eq for _, eq, _, _ in poly)
    max_r = max(er for _, _, er, _ in poly)
    max_s = max(es for _, _, _, es in poly)

    p_pows = _power_table(p, max_p)
    q_pows = _power_table(q, max_q)
    r_pows = _power_table(r, max_r)
    s_pows = _power_table(s, max_s)

    return _eval_compiled_sparse_4var(
        _compile_sparse_4var(poly),
        p_pows=p_pows,
        q_pows=q_pows,
        r_pows=r_pows,
        s_pows=s_pows,
    )


def _normalize_expr(expr: str) -> str:
    expr = expr.replace("−", "-").replace("–", "-")
    expr = expr.replace(" ", "").replace("\n", "")
    return expr


def parse_sparse_4var_expr(expr: str) -> Sparse4VarPoly:
    r"""Parse a sum of monomials in p,q,r,s into a sparse exact dictionary.

    Accepted term shapes include examples such as:
      -100p7q7
      +6075p11qs2
      -375000q2r2
      +74218750qs4

    Exponents omitted after a variable mean exponent 1.
    """
    expr = _normalize_expr(expr)
    if not expr:
        return {}
    if expr[0] not in '+-':
        expr = '+' + expr
    parts = _TERM_SPLIT_RE.findall(expr)
    out: Sparse4VarPoly = {}
    for part in parts:
        sign = -1 if part[0] == '-' else 1
        body = part[1:]

        m = _COEFF_RE.match(body)
        if m:
            coeff = sign * int(m.group(1))
            body = body[m.end():]
        else:
            coeff = sign

        exps = {'p': 0, 'q': 0, 'r': 0, 's': 0}
        for var, exp_s in _VAR_EXP_RE.findall(body):
            exps[var] += int(exp_s) if exp_s else 1

        key = (exps['p'], exps['q'], exps['r'], exps['s'])
        out[key] = out.get(key, Fraction(0, 1)) + Fraction(coeff)
    return out


def _prepare_power_tables(
    *,
    p: Fraction,
    q: Fraction,
    r: Fraction,
    s: Fraction,
) -> tuple[list[Fraction], list[Fraction], list[Fraction], list[Fraction]]:
    max_p, max_q, max_r, max_s = _GLOBAL_MAX_EXP
    return (
        _power_table(p, max_p),
        _power_table(q, max_q),
        _power_table(r, max_r),
        _power_table(s, max_s),
    )


def _eval_theta_linear_combination_compiled(
    block: tuple[CompiledSparse4VarPoly, ...],
    theta: Fraction,
    *,
    p_pows: list[Fraction],
    q_pows: list[Fraction],
    r_pows: list[Fraction],
    s_pows: list[Fraction],
) -> Fraction:
    total = Fraction(0, 1)
    for poly in reversed(block):
        total = total * theta + _eval_compiled_sparse_4var(
            poly,
            p_pows=p_pows,
            q_pows=q_pows,
            r_pows=r_pows,
            s_pows=s_pows,
        )
    return total


