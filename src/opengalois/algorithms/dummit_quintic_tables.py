def eval_b4(index: int, *, p: Fraction, q: Fraction, r: Fraction, s: Fraction) -> Fraction:
    """Evaluate b4[index] = b_{40+index} exactly."""
    p_pows, q_pows, r_pows, s_pows = _prepare_power_tables(p=p, q=q, r=r, s=s)
    return _eval_compiled_sparse_4var(
        B4_COMPILED[index],
        p_pows=p_pows,
        q_pows=q_pows,
        r_pows=r_pows,
        s_pows=s_pows,
    )


def eval_T4(*, p: Fraction, q: Fraction, r: Fraction, s: Fraction,
            theta: Fraction, D: Fraction) -> Fraction:
    """Evaluate Dummit's T4 coefficient exactly.

    T4 = (b40 + b41*theta + ... + b45*theta^5) / (2*D*F)
    """
    p_pows, q_pows, r_pows, s_pows = _prepare_power_tables(p=p, q=q, r=r, s=s)
    num = _eval_theta_linear_combination_compiled(
        B4_COMPILED,
        theta,
        p_pows=p_pows,
        q_pows=q_pows,
        r_pows=r_pows,
        s_pows=s_pows,
    )
    den = 2 * D * _eval_compiled_sparse_4var(
        F_COMPILED,
        p_pows=p_pows,
        q_pows=q_pows,
        r_pows=r_pows,
        s_pows=s_pows,
    )
    if den == 0:
        raise ZeroDivisionError('Dummit appendix denominator 2*D*F vanished.')
    return num / den


